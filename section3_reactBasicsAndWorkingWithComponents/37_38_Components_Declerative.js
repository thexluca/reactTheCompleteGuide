
// *** 37. WHAT ARE COMPONENTS *** 

// Reusable building blocks in your user interface. Components are just a 
// combination of HTML code, CSS code for styling and JavaScript for logic. 
// React is all bout components, you build individual components and then you 
// tell React how to compose them together into a final user interface.

// ** WHY USE COMPONENTS? ** 
// 1. Reusability
// Having reusable building blocks helps us avoid repetition.
// 2. Separation of Concerns
// Keep codebase small and manageble, every component has one clear and simple 
// concern. This creates small pieces of code that are easy to manage and maintian. 


